void solve() {
    string s;
    cin >> s;
    if(s[0] == '0') {
        cout << "0\n";
        return;
    }
    int cnt = 1;
    for(int i = 0; i < s.size(); i ++) {
        if(s[i] == '?') {
            if(!i) cnt *= 9;
            else cnt *= 10;
        }
    }
    cout << cnt << '\n';
}


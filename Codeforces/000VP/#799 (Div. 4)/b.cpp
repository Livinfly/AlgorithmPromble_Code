void solve() {
    int n;
    cin >> n;
    vector<int> a(n);
    map<int, int> mp;
    for(auto &x : a) 
        cin >> x, mp[x] ++;
    int sum = 0;
    for(auto it : mp) {
        int x = it.second-1;
        sum += x;
    }
    cout << n-(sum+1)/2*2 << '\n';
}

